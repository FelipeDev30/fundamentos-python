# Aula 3 - Variáveis e Constantes

# O que são Variáveis?
# Variáveis são espaços na memória do computador onde podemos armazenar dados que podem ser alterados durante a execução do programa.
# Exemplo de declaração e uso de variáveis em Python:
idade = 25  # Variável que armazena a idade
nome = "João"  # Variável que armazena o nome
print("Nome:", nome)
print("Idade:", idade)

# O que são Constantes?
# Constantes são valores que não mudam durante a execução do programa. Em Python, não há uma sintaxe específica para declarar constantes, mas por convenção, usamos letras maiúsculas.
PI = 3.14159  # Constante que armazena o valor de pi
GRAVIDADE = 9.81  # Constante que armazena o valor da gravidade na Terra
print("Valor de PI:", PI)
print("Valor da Gravidade:", GRAVIDADE)

